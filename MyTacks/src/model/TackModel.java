package model;

public class TackModel {
	private String tackName;
	private String tackURL;
	private String tackDescription;
	public String getTackName() {
		return tackName;
	}
	public void setTackName(String tackName) {
		this.tackName = tackName;
	}
	public String getTackURL() {
		return tackURL;
	}
	public void setTackURL(String tackURL) {
		this.tackURL = tackURL;
	}
	public String getTackDescription() {
		return tackDescription;
	}
	public void setTackDescription(String tackDescription) {
		this.tackDescription = tackDescription;
	}
	

}
